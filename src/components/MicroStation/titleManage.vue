<template>
  <div style="position: relative; top: 30%; transform: translateY(-30%)">
    <el-form :model="ruleForm" :rules="rules" ref="ruleForm" label-width="100px" class="demo-ruleForm">
      <el-form-item label="主标题" prop="mainTitle">
        <el-input v-model="ruleForm.mainTitle"></el-input>
      </el-form-item>
      <el-form-item label="副标题" prop="miniTitle">
        <el-input v-model="ruleForm.miniTitle"></el-input>
      </el-form-item>
      <el-form-item>
        <el-button type="primary" @click="submitForm('ruleForm')">保存</el-button>
        <el-button @click="resetForm('ruleForm')">返回</el-button>
      </el-form-item>
    </el-form>
  </div>
</template>

<script>
export default {
  data() {
    return {
      ruleForm: {
        mainTitle: '',
        miniTitle: ''
      },
      rules: {
        mainTitle: [
          { required: true, message: '请输入主标题', trigger: 'blur' },
          { min: 2, max: 100, message: '长度在 2 到 100 个字符', trigger: 'blur' }
        ]
      }
    }
  },
  methods: {
    submitForm(formName) {
      this.$refs[formName].validate((valid) => {
        if (valid) {
          alert('submit!')
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    resetForm(formName) {
      this.$refs[formName].resetFields()
    }
  }
}
</script>

